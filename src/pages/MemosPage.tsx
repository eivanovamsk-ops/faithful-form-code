const MemoCard = ({ memo, index }: { memo: Memo; index: number }) => {
  const [isOpen, setIsOpen] = useState(false);

  return (
    <motion.div
      initial={{ opacity: 0, y: 30 }}
      whileInView={{ opacity: 1, y: 0 }}
      viewport={{ once: true, margin: "-30px" }}
      transition={{ duration: 0.5, delay: index * 0.08 }}
      className="bg-card rounded-2xl border border-border overflow-hidden hover:border-brand-teal/30 hover:shadow-[0_8px_40px_-12px_hsl(var(--brand-teal)/0.15)] transition-all duration-500"
    >
      <button
        onClick={() => setIsOpen(!isOpen)}
        className="w-full text-left p-8 flex items-start gap-5"
      >
        <div className={cn(
          "w-14 h-14 rounded-2xl flex items-center justify-center shrink-0 transition-all duration-500",
          isOpen ? "bg-brand-teal scale-110" : "bg-secondary"
        )}>
          <memo.icon className={cn(
            "w-6 h-6 transition-colors duration-500",
            isOpen ? "text-primary-foreground" : "text-brand-blue"
          )} />
        </div>
        <div className="flex-1 min-w-0">
          <h2 className={cn(
            "font-display font-semibold text-lg mb-2 transition-colors duration-300 pr-8",
            isOpen ? "text-brand-teal" : "text-foreground"
          )}>
            {memo.title}
          </h2>
          <p className="text-muted-foreground text-sm leading-relaxed">{memo.intro}</p>
        </div>
        <ChevronDown className={cn(
          "w-5 h-5 text-muted-foreground shrink-0 mt-1 transition-transform duration-300",
          isOpen && "rotate-180"
        )} />
      </button>

      <AnimatePresence>
        {isOpen && (
          <motion.div
            initial={{ height: 0, opacity: 0 }}
            animate={{ height: "auto", opacity: 1 }}
            exit={{ height: 0, opacity: 0 }}
            transition={{ duration: 0.3 }}
            className="overflow-hidden"
          >
            <div className="px-8 pb-8 space-y-8 border-t border-border pt-6 ml-[4.75rem]">
              {memo.sections.map((section, si) => (
                <motion.div
                  key={section.title}
                  initial={{ opacity: 0, y: 10 }}
                  animate={{ opacity: 1, y: 0 }}
                  transition={{ delay: si * 0.05 }}
                >
                  <h3 className="font-display font-semibold text-base text-foreground mb-3">{section.title}</h3>
                  {section.intro && (
                    <p className="text-sm text-foreground/80 mb-3 leading-relaxed">{section.intro}</p>
                  )}
                  <ul className="space-y-2.5">
                    {section.items.map((item, ii) => (
                      <li key={ii} className="flex items-start gap-3 text-sm text-foreground/80">
                        <CheckCircle className="w-4 h-4 text-brand-teal shrink-0 mt-0.5" />
                        {item}
                      </li>
                    ))}
                  </ul>
                </motion.div>
              ))}
            </div>
          </motion.div>
        )}
      </AnimatePresence>
    </motion.div>
  );
};

